    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
      String placa = txtPlaca.getText().trim();

    if (placa.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "‚ö†Ô∏è Debes ingresar una placa para retirar.");
        return;
    }

    // üîπ Llamar al proceso de retiro (esto debe devolverte el ticket)
    Ticket ticket = RegistroEntrada.retirarVehiculo(placa);

    // üîπ Validar si se encontr√≥ un ticket
    if (ticket == null) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "‚ùå No se encontr√≥ un ticket activo para la placa: " + placa);
        return;
    }

    // üîπ Cerrar el ticket (registrar salida y monto)
    ticket.cerrarTicket();

    // üîπ Mostrar mensaje con el monto total
    javax.swing.JOptionPane.showMessageDialog(this, 
        "‚úÖ Veh√≠culo retirado correctamente.\n" +
        "Monto total a pagar: Q" + String.format("%.2f", ticket.getMonto()) +
        "\n\nGenerando factura PDF...");

    // üîπ Generar directamente la factura PDF
    ticket.generarTicketPDF();

    // üîπ Limpiar campo de placa
    txtPlaca.setText("");

    }//GEN-LAST:event_btnRetirarActionPerformed

    private void btnRetirarFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarFActionPerformed
        String placa = txtPlaca.getText().trim();

    if (placa.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "‚ö†Ô∏è Debes ingresar una placa para retirar (modo FLAT).");
        return;
    }

    Ticket ticket = RegistroEntrada.retirarVehiculo(placa);

    if (ticket == null) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "‚ùå No se encontr√≥ un ticket activo para la placa: " + placa);
        return;
    }

    // Cerrar ticket y aplicar reglas de FLAT
    ticket.cerrarTicket();

    if (ticket.getMonto() == 0) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "‚úÖ Reingreso v√°lido (a√∫n dentro de las 2 horas). No se cobra nuevamente.");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this,
            "‚ö†Ô∏è Tiempo excedido de 2 horas.\n" +
            "Se aplicar√° una nueva tarifa de Q" + String.format("%.2f", ticket.getMonto()) +
            "\n\nGenerando factura PDF...");
        ticket.generarTicketPDF();
    }

    txtPlaca.setText("");
    }//GEN-LAST:event_btnRetirarFActionPerformed


