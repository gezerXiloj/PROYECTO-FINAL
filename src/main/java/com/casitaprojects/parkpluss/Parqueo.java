    private void btnReingresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReingresoActionPerformed
String placa = JOptionPane.showInputDialog(this, "Ingrese la placa del veh√≠culo:");

    if (placa == null || placa.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Debes ingresar una placa.");
        return;
    }

Ticket ultimo = RegistroEntrada.buscarTicketPorPlaca(placa.trim());
if (ultimo == null) {
    JOptionPane.showMessageDialog(this, "‚ùå No se encontr√≥ ning√∫n registro anterior de esa placa.");
    return;
}

if (ultimo.getModoTarifa() != ModoTarifa.FLAT) {
    JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Este veh√≠culo no tiene modo FLAT.");
    return;
}

if (ultimo.getFechaSalida() == null) {
    JOptionPane.showMessageDialog(this, "‚ö†Ô∏è El veh√≠culo a√∫n est√° activo dentro del parqueo.");
    return;
}

    // Verificamos que el ticket tenga modo FLAT
    if (ultimo.getModoTarifa() == ModoTarifa.FLAT) {

        // Si el ticket no tiene fecha de salida, significa que a√∫n est√° activo
        if (ultimo.getFechaSalida() == null) {
            JOptionPane.showMessageDialog(this,
                "üöó El veh√≠culo a√∫n est√° dentro del parqueo.\nNo se puede registrar reingreso.");
            return;
        }

        // Calculamos el tiempo desde la √∫ltima salida
        LocalDateTime salida = ultimo.getFechaSalida();
        long minutos = java.time.Duration.between(salida, LocalDateTime.now()).toMinutes();

        if (minutos <= 120) {
            JOptionPane.showMessageDialog(this,
                "‚úÖ Reingreso v√°lido: a√∫n dentro del tiempo del ticket FLAT.\nNo se cobrar√° nuevamente.");

            Vehiculo v = new Vehiculo(placa.trim(), TipoVehiculo.AUTO, TipoArea.ESTUDIANTES);
            RegistroEntrada.registrarVehiculo(v, ModoTarifa.FLAT);
            return;

        } else {
            JOptionPane.showMessageDialog(this,
                "‚åõ Tiempo del ticket FLAT expirado. Se generar√° un nuevo cobro.");
            Vehiculo v = new Vehiculo(placa.trim(), TipoVehiculo.AUTO, TipoArea.ESTUDIANTES);
            Ticket nuevoTicket = RegistroEntrada.registrarVehiculo(v, ModoTarifa.FLAT);
            if(nuevoTicket != null){
                GestorDB.insertarTicket(nuevoTicket);
            }
            return;
        }
    } else {
        JOptionPane.showMessageDialog(this,
            "‚ö†Ô∏è Este veh√≠culo no tiene tarifa FLAT registrada.");
    }

    }//GEN-LAST:event_btnReingresoActionPerformed

